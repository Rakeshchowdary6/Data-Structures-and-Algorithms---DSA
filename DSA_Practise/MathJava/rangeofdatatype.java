package DSA_Practise.MathJava;

public class rangeofdatatype {
    public static void main(String[] args) {
        int positiverange = (int) Math.pow(2,31) ;
        int negativerange = -(int) Math.pow(2,31);

       System.out.println(positiverange);
       System.out.println(negativerange);

    }
}
